제 1 조 (목적)
이 이용약관(이하 '약관'이라 합니다)은 맥가닷컴(이하 '사이트'라 합니다)과 이용 회원간에 사이트가 제공하는 맥가닷컴 자료사용과 가입조건 및 이용에 관한 제반 사항과 기타 필요한 사항을 구체적으로 규정함을 목적으로 합니다.

제 2 조 (용어의 정의)
(1) 이 약관에서 사용하는 용어의 정의는 다음과 같습니다. 
- '회원'이라 함은 이 약관에 동의하고 서비스를 이용하는 이용자를 말합니다.
- '이용계약'이라 함은 이 약관을 포함하여 서비스 이용과 관련하여 사이트와 회원 간에 체결하는 모든 계약을 말합니다. 
- '이용자ID'라 함은 회원의 식별 및 서비스 이용을 위하여 회원의 신청에 따라 사이트가 회원별로 부여하는 고유한 문자와 숫자의 조합을 말합니다. 
- '비밀번호'라 함은 이용자ID로 식별되는 회원의 본인 여부를 검증하기 위하여 회원이 설정하여 사이트에 등록한 고유의 문자와 숫자의 조합을 말합니다. 
- '단말기'라 함은 서비스에 접속하기 위해 회원이 이용하는 개인용 컴퓨터, PDA, 휴대전화 등의 전산장치를 말합니다. 
- '해지'라 함은 사이트 또는 회원이 이용계약을 해약하는 것을 말합니다.
(2) 이 약관에서 사용하는 용어 중 제1항에서 정하지 아니한 것은 관계 법령 및 서비스별 안내에서 정하는 바에 따르며, 그 외에는 일반 관례에 따릅니다.

제 3 조 (이용약관의 효력 및 변경) 
(1) 이 약관은 맥가닷컴을 통해 온라인으로 공시하고 회원의 동의와 사이트의 승낙으로 효력을 발생하며, 합리적인 사유가 발생할 경우 사이트는 관련 법령에 위배되지 않는 범위 안에서 개정할 수 있습니다. 개정된 약관은 정당한 절차에 따라 맥가닷컴을 통해 공지함으로써 효력을 발휘합니다.
(2) 회원은 정기적으로 맥가닷컴을 방문하여 약관의 변경사항을 확인하여야 합니다. 변경된 약관에 대한 정보를 알지 못해 발생하는 회원의 피해는 사이트에서 책임지지 않습니다.
(3) 회원은 변경된 약관에 동의하지 않을 경우 회원 탈퇴(해지)를 요청할 수 있습니다.

제 4 조 (약관외 준칙)
사이트는 필요한 경우 서비스 내의 개별항목에 대하여 개별약관 또는 운영원칙(이하 '서비스별 안내'라 합니다)를 정할 수 있으며, 이 약관과 서비스별 안내의 내용이 상충되는 경우에는 서비스별 안내의 내용을 우선하여 적용합니다.


